    public static byte[] concatByteArrays(byte[]... arrays) {
      int destIndex = 0;
      int destLength = 0;
      for (byte[] a : arrays)
        destLength += a.length;
      byte[] bytes = new byte[destLength];
      for (byte[] src : arrays) {
        System.arraycopy(src, 0, bytes, destIndex, src.length);
        destIndex += src.length;
      }
      return bytes;
    }

